package app.utils;

public class Vehicle {
    String company;
    String model;
    int id;
    int year;
    String comment;

    public Vehicle(String company, String model, int id, int year, String comment) {
        this.company = company;
        this.model = model;
        this.id = id;
        this.year = year;
        this.comment = comment;
    }
}
